#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	srclen;
	size_t	dstlen;

	i = 0;
	srclen = ft_strlen(src);
	if (!dstsize || dstsize <= ft_strlen(dst))
		return (srclen + dstsize);
	dstlen = ft_strlen(dst);
	while (src[i] && dstsize > dstlen + 1 + i)
	{
		dst[dstlen + i] = src[i];
		i++;
	}
	dst[dstlen + i] = '\0';
	return (dstlen + srclen);
}
